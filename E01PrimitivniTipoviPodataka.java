package edunova;

public class E01PrimitivniTipoviPodataka {
	
	public static void main(String[] args) {
		
		//deklariranje varijable tip integer
		int i;
		
		//dodjeljivanje vrijednosti
		i=2147483647;
		
		i=i+1;
		
		//kori≈°tenje varijable
		System.out.println(i);
		
		//https://docs.oracle.com/javase/tutorial/java/nutsandbolts/datatypes.html
		
		//DZ svaki primitivni tip deklarirati, dodjeliti vrijednost i ispisati
		
		
		byte a;
		a=12;
		
		System.out.println(a);
		
		float b;
		b=(float)3.01;
		System.out.println(b);
		
		double x = 41.4;
		
		System.out.println(x);
	
		
	}

}
